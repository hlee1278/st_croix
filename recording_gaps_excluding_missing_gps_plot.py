def read_file(path):
    ext = os.path.splitext(path)[1].lower()
    return pd.read_csv(path) if ext == '.csv' else pd.read_excel(path)


# Read a CSV or Excel file into a pandas DataFrame.

def parse_times(series):
    parsed = pd.to_datetime(series, errors='coerce', format='mixed')
    if parsed.isna().all():
        return pd.to_numeric(series, errors='coerce'), False
    return parsed, True


# Attempt to parse a time column into datetime values.
# Returns:
#     parsed_times : datetime or numeric series
#     is_datetime  : True if parsed as datetime

def load_group(files):
    loaded = []
    for path in files:
        name = os.path.basename(path)
        try:
            df = read_file(path)
        except Exception as e:
            print(f"Warning: Could not read '{name}': {e}")
            continue
        if 'time' not in df.columns or 'temp_probe' not in df.columns:
            print(
                f"Warning: '{name}' missing 'time' or 'temp_probe'. Skipping.")
            continue
        df = df.dropna(subset=['time', 'temp_probe']).copy()
        if df.empty:
            continue
        t, is_dt = parse_times(df['time'])
        df['_t'] = t
        df = df.dropna(subset=['_t']).sort_values('_t').reset_index(drop=True)
        if df.empty:
            continue
        loaded.append((name, df, is_dt))
    return loaded


