# SECTION 7: BUILD COVERAGE SUMMARY TEXT
# =============================================================================

def build_summary_text(am_regions, pm_regions, am_count, pm_count):
    """
    Group all 'partial' messages together, then all 'none' messages.
    Skip partial entries for blocks that only have 1 file (not meaningful).
    Kestrel stop periods never appear here, since find_gap_regions already
    excludes them from the 'none'/'partial' classification.
    """
    partial_lines = []
    none_lines = []

    for block_label, regions, n_files in [
        ('AM', am_regions, am_count),
        ('PM', pm_regions, pm_count),
    ]:
        for r in regions:
            line = f"  [{block_label}]  {fmt_time(r['start'])} → {fmt_time(r['end'])}"
            if r['type'] == 'partial' and n_files >= 2:
                partial_lines.append(line)
            elif r['type'] == 'none':
                none_lines.append(line)

    parts = []
    if partial_lines:
        parts.append(
            "At least one pocket lab didn't collect data for time ranging:")
        parts.extend(partial_lines)
    if none_lines:
        if parts:
            parts.append("")
        parts.append("No pocket lab collected data for time ranging:")
        parts.extend(none_lines)

    return "\n".join(parts) if parts else "No coverage gaps detected."


def plot_temp_vs_time(folder):
    all_files = load_files(folder)
    if not all_files:
        print(f"No spreadsheet files found in: {folder}")
        return

    # Current folder = all_R13
    # Parent folder = 0617_R13

    parent_folder = os.path.basename(
        os.path.dirname(os.path.normpath(folder))
    )

    run_label = parent_folder

    # If a kestrel file happens to live in the same FOLDER, don't let the
    # automatic scan above also pick it up and try to read it as a
    # pocket-lab file (it has a different layout and will just fail).
    kestrel_path_set = {os.path.abspath(p) for p in KESTREL_FILES}

    am_files, pm_files, other_files = [], [], []
    for path in all_files:
        if os.path.abspath(path) in kestrel_path_set:
            continue
        name = os.path.basename(path).upper()
        if 'AM' in name:
            am_files.append(path)
        elif 'PM' in name:
            pm_files.append(path)
        else:
            other_files.append(path)

    if other_files:
        print("Note: files without 'AM' or 'PM' in name will be skipped:")
        for f in other_files:
            print(f"  {os.path.basename(f)}")

    # ── Sort kestrel files into AM / PM by filename ──
    kestrel_am, kestrel_pm, kestrel_other = [], [], []
    for path in KESTREL_FILES:
        name = os.path.basename(path).upper()
        if 'AM' in name:
            kestrel_am.append(path)
        elif 'PM' in name:
            kestrel_pm.append(path)
        else:
            kestrel_other.append(path)

    if kestrel_other:
        print("Note: kestrel files without 'AM' or 'PM' in name will be skipped:")
        for f in kestrel_other:
            print(f"  {os.path.basename(f)}")

    fig = plt.figure(figsize=(14, 15))
    ax_am = fig.add_axes([0.08, 0.52, 0.88, 0.34])
    ax_pm = fig.add_axes([0.08, 0.04, 0.88, 0.34])

    fig.suptitle(
        f'Time vs Temp Probe  —  {run_label}',
        fontsize=14,
        fontweight='bold',
        y = 0.995
    )

    fig.text(
        0.5,  # centered horizontally
        0.93,  # slightly below main title
        'All pocket lab data, regardless of GPS coordinates',
        ha='center',
        fontsize=10,
        style='italic',
        color='dimgray'
    )

    am_regions, am_handles, am_labels = plot_group(
        ax_am, am_files, 'Morning Block (AM)',
        kestrel_paths=kestrel_am, axis_start_time=AXIS_START_AM)
    pm_regions, pm_handles, pm_labels = plot_group(
        ax_pm, pm_files, 'Afternoon Block (PM)',
        kestrel_paths=kestrel_pm, axis_start_time=AXIS_START_PM)

    # ── Combine AM + PM legend entries into a single figure-level legend ──
    # (use each handle's own get_label() — patch handles aren't reflected
    # in the separate labels list returned by get_legend_handles_labels())
    combined = {}
    for handles in (am_handles, pm_handles):
        for h in handles:
            l = h.get_label()
            if l and l not in combined:
                combined[l] = h

    if combined:
        fig.legend(
            list(combined.values()), list(combined.keys()),
            loc='upper center',
            bbox_to_anchor=(0.5, 0.905),
            ncol=len(combined),
            frameon=True,
            fontsize=9,
        )

    summary = build_summary_text(am_regions, pm_regions,
                                 len(am_files), len(pm_files))
    print(f"\n[{run_label}] Coverage report:\n{summary}")

    fig.text(
        0.08, 0.004,
        summary,
        fontsize=8,
        verticalalignment='top',
        horizontalalignment='left',
        family='monospace',
        color='#333333',
    )

    output_name = f"recording_gaps_{run_label}.png"
    output_path = os.path.join(folder, output_name)
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    print(f"\nPlot saved to: {output_path}")
    plt.show()


