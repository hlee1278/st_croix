def fmt_time(t):
    if isinstance(t, pd.Timestamp):
        return t.strftime('%H:%M:%S')
    return str(t)


# Convert a time value into a readable string.

def set_time_axis(ax, is_datetime):
    """Show only time of day (no date)."""
    if not is_datetime:
        return

    ax.xaxis.set_major_formatter(mdates.DateFormatter('%H:%M'))
    ax.xaxis.set_major_locator(mdates.AutoDateLocator(minticks=4, maxticks=10))

    plt.setp(ax.xaxis.get_majorticklabels(), rotation=20, ha='right')


def plot_group(ax, files, title, kestrel_paths=None, axis_start_time=None):
    """
    kestrel_paths    : list of kestrel CSV paths belonging to this block
                        (AM or PM). Each becomes its own light-blue stop
                        block. May be empty/None.
    axis_start_time  : datetime.time to use as the fixed left edge of the
                        x-axis (e.g. 9:45 AM for the AM plot). May be None.

    Returns (regions, handles, labels). No legend is drawn on the axes
    itself — handles/labels are returned so the caller can combine AM and
    PM into a single figure-level legend.
    """
    kestrel_paths = kestrel_paths or []
    loaded = load_group(files)
    n_files = len(loaded)

    if not loaded:
        ax.set_title(title, pad = 12)
        ax.text(0.5, 0.5, 'No valid files', ha='center', va='center',
                transform=ax.transAxes)
        return [], [], []

    is_datetime = any(is_dt for _, _, is_dt in loaded)
    all_times = pd.concat([df['_t'] for _, df, _ in loaded])
    global_min = all_times.min()

    # ── Build "stop period" windows from kestrel files (light blue) ──
    stop_intervals = []
    if kestrel_paths and is_datetime:
        ref_date = global_min.date()
        for path in kestrel_paths:
            result = read_kestrel_stop_period(path)
            if result is None:
                continue
            start_t, end_t = result
            start_dt = pd.Timestamp.combine(ref_date, start_t)
            end_dt = pd.Timestamp.combine(ref_date, end_t)
            stop_intervals.append((start_dt, end_dt))
    elif kestrel_paths and not is_datetime:
        print(f"Warning: '{title}' timestamps aren't recognized as dates/"
              f"times — skipping kestrel stop-period overlay.")

    # ── Plot lines, split at gaps so no connecting lines ──
    for name, df, is_dt in loaded:
        threshold = (pd.Timedelta(seconds=GAP_THRESHOLD_SECONDS)
                     if is_dt else GAP_THRESHOLD_SECONDS)
        gap_indices = df.index[df['_t'].diff() > threshold].tolist()
        split_points = [0] + gap_indices + [len(df)]

        color = None
        for i in range(len(split_points) - 1):
            seg = df.iloc[split_points[i]:split_points[i + 1]]
            if seg.empty:
                continue
            if color is None:
                parts = name.split("_")
                if len(parts) >= 4:
                    label = parts[3]  # TJ or BH
                else:
                    label = name
                line, = ax.plot(seg['_t'], seg['temp_probe'], label=label)
                color = line.get_color()
            else:
                ax.plot(seg['_t'], seg['temp_probe'], color=color)

    # ── Shade gap regions (kestrel stop periods are excluded from these) ──
    regions = find_gap_regions(loaded, stop_intervals=stop_intervals)

    has_none = False
    has_partial = False
    for r in regions:
        if r['type'] == 'none':
            ax.axvspan(r['start'], r['end'], color='red', alpha=0.25, zorder=0)
            has_none = True
        elif r['type'] == 'partial' and n_files >= 2:
            ax.axvspan(r['start'], r['end'], color='orange', alpha=0.20,
                       zorder=0)
            has_partial = True

    # ── Shade kestrel stop periods (drawn above red/orange) ──
    has_stop = False
    for start_dt, end_dt in stop_intervals:
        ax.axvspan(start_dt, end_dt, color='#77F79B', alpha=0.35, zorder=1)
        has_stop = True

    # ── Shade the 15 minutes immediately before the first kestrel reading ──
    has_pre_kestrel = False
    if stop_intervals:
        first_kestrel_start = min(start for start, _ in stop_intervals)
        pre_start = first_kestrel_start - pd.Timedelta(minutes=15)
        ax.axvspan(pre_start, first_kestrel_start, color='#CCDFFC',
                   alpha=1,
                   zorder=0.5)
        has_pre_kestrel = True


    ax.set_title(title, pad = 12)
    ax.set_xlabel('Time')
    ax.set_ylabel('Temp Probe')
    set_time_axis(ax, is_datetime)

    # ── Fixed axis start time (e.g. 9:45 AM / 1:45 PM) ──
    if is_datetime and axis_start_time is not None:
        left_bound = pd.Timestamp.combine(global_min.date(), axis_start_time)
        ax.set_xlim(left=left_bound)

    handles, labels = ax.get_legend_handles_labels()
    if has_stop:
        handles.append(mpatches.Patch(color='#77F79B', alpha=0.5,
                                      label='Pocket lab stop (kestrel reading)'))
    if has_pre_kestrel:
        handles.append(mpatches.Patch(color='#CCDFFC', alpha=0.6,
                                      label='15 min before first kestrel reading'))
    if has_partial:
        handles.append(mpatches.Patch(color='orange', alpha=0.4,
                                      label="Missing 1 lab's data"))
    if has_none:
        handles.append(mpatches.Patch(color='red', alpha=0.4,
                                      label='No lab has data'))

    return regions, handles, labels


