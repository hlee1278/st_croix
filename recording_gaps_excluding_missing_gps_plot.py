# SECTION 3B: READ KESTREL (WBGT) STOP-PERIOD FILES
# =============================================================================

def read_kestrel_stop_period(path):
    """
    Read a kestrel CSV and return the (start_time, end_time) span of its
    timestamp column, as datetime.time objects. Only wall-clock time
    matters here — the date in the kestrel file is ignored, since the
    stop block is placed using the date already present in the pocket
    lab data for that plot.

    Expected file layout:
        Rows 1-5 : header / instrument metadata (skipped)
        Row 6+   : data, with timestamps in the first column, e.g.
                   '6/7/2026  10:48:45 AM'   (note the double space)

    Returns None if the file can't be read or has no parseable timestamps.
    """
    name = os.path.basename(path)
    try:
        df = pd.read_csv(path, skiprows=5, header=None, usecols=[0])
    except Exception as e:
        print(f"Warning: Could not read kestrel file '{name}': {e}")
        return None

    raw = df.iloc[:, 0].dropna().astype(str)
    raw = raw.str.replace(r'\s+', ' ', regex=True).str.strip()
    parsed = pd.to_datetime(raw, errors='coerce', format='mixed').dropna()

    if parsed.empty:
        print(f"Warning: No valid timestamps found in kestrel file '{name}'.")
        return None

    return parsed.min().time(), parsed.max().time()


# =============================================================================
# SECTION 4: DETERMINE DATA COVERAGE BY SECOND
# =============================================================================

def classify_seconds(loaded):
    is_datetime = any(is_dt for _, _, is_dt in loaded)
    all_times = pd.concat([df['_t'] for _, df, _ in loaded])
    global_min = all_times.min()
    # Determine how many files contain data at each second.
    # Returns:
    #    global_min
    #        Earliest timestamp found
    #    global_max_sec
    #        Maximum elapsed second
    #    coverage
    #        Dictionary mapping:
    #            second -> number of files
    #                       containing data
    #    is_datetime
    #        True if timestamps are datetime values

    if is_datetime:
        def to_int_seconds(series):
            return ((series - global_min).dt.total_seconds()).astype(int)

        global_max_sec = int((all_times.max() - global_min).total_seconds())
    else:
        def to_int_seconds(series):
            return (series - global_min).astype(int)

        global_max_sec = int(all_times.max() - global_min)

    # coverage[sec] = number of distinct files with data at that second
    coverage = {}
    for name, df, is_dt in loaded:
        secs = to_int_seconds(df['_t'])
        for s in secs:
            coverage[s] = coverage.get(s, 0) + 1

    return global_min, global_max_sec, coverage, is_datetime


# =============================================================================
# SECTION 5: IDENTIFY GAP REGIONS
# =============================================================================

def find_gap_regions(loaded, stop_intervals=None):
    """
    Classify each second relative to the total number of files in this group:
      - 0 files   -> 'none'
      - 1..N-1    -> 'partial'  (some but not all; only meaningful when N >= 2)
      - N files   -> 'all'      (full coverage, no issue)

    When there is only 1 file, every second is either 'none' or 'all' —
    there is no 'partial' state.

    stop_intervals : optional list of (start, end) datetime/Timestamp pairs
        marking known "stop" periods (e.g. kestrel WBGT readings) during
        which the team was intentionally stationary. Any second inside
        one of these windows is always treated as 'all' (fully covered),
        even if the pocket labs themselves recorded nothing there — so
        it is never reported as a gap.

    Analyzes data coverage across all files and identifies periods where
    data collection was incomplete.
    """
    if not loaded:
        return []

    if stop_intervals is None:
        stop_intervals = []

    n_files = len(loaded)
    global_min, global_max_sec, coverage, is_datetime = classify_seconds(loaded)

    def sec_to_time(s):
        if is_datetime:
            return global_min + pd.Timedelta(seconds=int(s))
        else:
            return global_min + int(s)

    def dt_to_sec(dt):
        if is_datetime:
            return int((dt - global_min).total_seconds())
        else:
            return int(dt - global_min)

    # Convert stop intervals into second-offsets and clip to the data range.
    stop_ranges_sec = []
    for start_dt, end_dt in stop_intervals:
        s_sec, e_sec = dt_to_sec(start_dt), dt_to_sec(end_dt)
        if e_sec < s_sec:
            s_sec, e_sec = e_sec, s_sec
        s_sec = max(s_sec, 0)
        e_sec = min(e_sec, global_max_sec)
        if s_sec <= e_sec:
            stop_ranges_sec.append((s_sec, e_sec))

    def in_stop(s):
        return any(start <= s <= end for start, end in stop_ranges_sec)

    regions = []
    current_type = None
    run_start = 0

    for s in range(global_max_sec + 1):
        if in_stop(s):
            stype = 'all'  # kestrel stop period — never a gap
        else:
            n = coverage.get(s, 0)
            if n == 0:
                stype = 'none'
            elif n < n_files:
                stype = 'partial'  # only relevant when n_files >= 2
            else:
                stype = 'all'

        if stype != current_type:
            if current_type in ('none', 'partial'):
                regions.append({
                    'type': current_type,
                    'start': sec_to_time(run_start),
                    'end': sec_to_time(s),
                })
            current_type = stype
            run_start = s

    if current_type in ('none', 'partial'):
        regions.append({
            'type': current_type,
            'start': sec_to_time(run_start),
            'end': sec_to_time(global_max_sec),
        })

    return regions


